public enum GroupCode {
	BOARD("1000", "게시구분"),
	DISCLOSURE_TYPE("1001", "적발유형"),
	HOUR("1002", "시"),
	MINUTE("1003", "분"),
	CAR_TYPE("1004", "차종"),
	FUEL("1005", "연료"),
	COMPANY("1006", "제조사"),
	NATION("1007", "국가"),
	USE("1008", "용도"),
	CAR_KIND("1009", "차종유형");

	private String groupCode;
	private String codeName;

	GroupCode(String groupCode, String codeName) {
		this.groupCode = groupCode;
		this.codeName = codeName;
	}

	public String getGroupCode() {
		return groupCode;
	}

	public String getCodeName() {
		return codeName;
	}
}
